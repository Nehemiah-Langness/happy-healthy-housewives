// Ingredients:
// • *One box of Dreamfields Elbow noodles
// • Two cans of tuna, drained
// • 1 Green Bell Pepper
// • 2 Stalks of Celery
// • ½ an Onion
// • 1 and ½ tsp of mineral salt
// • 1 tsp of Black Pepper
// • ** 1 Cup Mayo

// Directions:
// In a large pot, boil pasta according to directions on box. Drain and rinse under cold
// water to cool, then set aside.
// While pasta is cooking and cooling, chop pepper, celery, and onion into bite sized
// pieces.
// In a small fry pan, sauté onion for a few minutes with a bit of oil (this is an
// optional step but takes away a bit of the bite of raw onion).
// In a large mixing bowl, combine all ingredients. Mix well. Place in a sealed
// container and put in fridge a half hour before desired eating time.

